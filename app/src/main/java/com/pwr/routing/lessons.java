package com.pwr.routing;


public class lessons {
    private lessons[] response;
    private String poczatek;
    private String koniec;
    private String numer;
    private String nazwa;
    private String budynek;
    private String parzystosc;
    private String imie_nazwisko;





    public String getPoczatek() {
        return poczatek;
    }

    public void setPoczatek(String poczatek) {
        this.poczatek = poczatek;
    }

    public String getKoniec() {
        return koniec;
    }

    public void setKoniec(String koniec) {
        this.koniec = koniec;
    }

    public String getNumer() {
        return numer;
    }

    public void setNumer(String numer) {
        this.numer = numer;
    }

    public String getBudynek() {
        return budynek;
    }

    public void setBudynek(String budynek) {
        this.budynek = budynek;
    }

    public String getParzystosc() {
        return parzystosc;
    }

    public void setParzystosc(String parzystosc) {
        this.parzystosc = parzystosc;
    }

    public String getImie_nazwisko() {
        return imie_nazwisko;
    }

    public void setImie_nazwisko(String imie_nazwisko) {
        this.imie_nazwisko = imie_nazwisko;
    }

    public lessons[] getResponse() {
        return response;
    }

    public void setResponse(lessons[] response) {
        this.response = response;
    }

    public String getNazwa() {
        return nazwa;
    }

    public void setNazwa(String nazwa) {
        this.nazwa = nazwa;
    }
}
